# Integer math, including string usage (characters for display purposes, not executed functions)
print("5+5=", 5+5) # outputs 5+5= 10
print("5-5+", 5-5)
print("5/5=", 5/5) #division, yields float
print("5/6=", 5/6) #division, yields float
print("5//6=", 5//6) #integer division that truncates the result. returns an integer
# modulus division (this yields the remainder of the division)
print("5%3=",5%3) #should return 2
print("3%5=",3%5) #should return 3 (essentially 0 remainder 3/5 so 3 is returned)
print("5*5=",5*5)
print("2**4=",2**4) #2 raised to the power of 4 (exponent)